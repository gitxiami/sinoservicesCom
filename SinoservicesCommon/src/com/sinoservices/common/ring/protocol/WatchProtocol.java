package com.sinoservices.common.ring.protocol;

public class WatchProtocol {
	// 1. 背光：点亮手表背光 1000ms
	public static final byte[] backLightCmd 		= {0x40, 0x04, 0x73, 0x01, (byte) 0xE8, 0x03, 0x63, 0x2A};	
	// 2. 震动：开启手表震动 100ms
	public static final byte[] shakeCmd				= {0x40, 0x04, 0x73, 0x02, 0x64, 0x00, (byte) 0xDD, 0x2A};	
	// 3. 语音：发出与索引值为 0x0001 和 0x0002 相对应的音
	public static final byte[] voiceCmd				= {0x40, 0x07, 0x73, 0x03, 0x02, 0x01, 0x00, 0x02, 0x00, (byte) 0x82, 0x2A};	
	// 4. 显示：以坐标（0x00,0x00）起始正常显示字体为 32x16 长度为 2 的字符串“01”
	public static final byte[] dispDataCmd			= {0x40, 0x09, 0x73, 0x04, 0x00, 0x00, 0x00, 0x00, 0x02, 0x30, 0x31, (byte) 0xE3, 0x2A};
	// 5. 语音和显示:震动时间为 200ms,背光时间为 2000ms,语音长度为 1，索引为 0x01，反显字符，字体为 64x32，起始坐标为（0x00,0x00），长度为 1，字符为“2”
	public static final byte[] voiceAndDispDataCmd	= {0x40, 0x0F, 0x73, 0x05, (byte) 0xC8, 0x00, (byte) 0xD0, 0x07, 0x01, 0x01, 0x00, 0x01, 0x01, 0x00, 0x00, 0x01, 0x32, 0x5D, 0x2A};
	// 6. 设置时间:设置手表系统时间为 2015.01.20，12：10
	public static final byte[] setTimeCmd			= {0x40, 0x08, 0x73, 0x06, 0x07, (byte) 0xDF, 0x01, 0x14, 0x0C, 0x0A, (byte) 0x92, 0x2A};
	// 7. 获取电量：以百分比方式获取手表电量
	public static final byte[] getPowerCmd			= {0x40, 0x03, 0x73, 0x07, 0x00, 0x7D, 0x2A};
	// 9. 版本信息：
	public static final byte[] versionInfoCmd		= {0x40, 0x03, 0x73, 0x09, 0x00, 0x7F, 0x2A};
	
	// 10. 测试数字，字母命令：(*^__^*)123abcAB
	public static final byte[] testCmd				= {0x28, 0x2A, 0x5E, 0x5F, 0x5F, 0x5E, 0x2A, 0x29, 0x31, 0x32, 0x33, 0x61, 0x62, 0x63, 0x41, 0x42};
	
	// 手表红键、绿键返回数据
	public static final String RED_KEY_MSG		= "RED_KEY";
	public static final String GREEN_KEY_MSG	= "GREEN_KEY";
	
	// 手表蓝牙名称的首字母
	public static final String WATCH_NAME_FIRST_LETTER	= "W";
	
	
	static public final byte FRAME_START = 0x40;
	
	
	
}
